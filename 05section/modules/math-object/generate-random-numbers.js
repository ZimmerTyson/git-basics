// Math.random() - Generates a random decimal number between 0 - 1

console.log("Math.random() -------");
console.log(Math.random());

const randomNum = Math.floor(Math.random() * 10) + 1; // takes the random number generated by Math.random and multiples it by a whole number.
// Then it will round the decimal down to the lowest integer and add one so that it cannot equal 0;

console.log(randomNum);

function getRandomNumber(min, max) {
    return Math.floor(Math.random() * (max - min + 1) + min);
}

console.log(getRandomNumber(4, 15));
